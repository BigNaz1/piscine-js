export function findIP(str) {
    const ipRegex = /\b(?:(?:25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9]?[0-9])\.){3}(?:25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9]?[0-9])(?::(?:[1-9]\d{0,3}|[1-5]\d{4}|6(?:[0-4]\d{3}|5(?:[0-4]\d{2}|5(?:[0-2]\d|3[0-5])))))?(?!\d)\b/g;
    
    return str.match(ipRegex)
      .filter(ip => !ip.includes('255.255.255.000') && !ip.match(/^0+\./))
      .map(ip => ip.replace(/^0+(\d)/, '$1').replace(/\.0+(\d)/g, '.$1'));
  }